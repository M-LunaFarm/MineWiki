import { createHmac, X509Certificate } from 'node:crypto';

const HOSTNAME_PATTERN = /^(?=.{4,253}$)(?:[a-z0-9](?:[a-z0-9-]{0,61}[a-z0-9])?\.)+[a-z](?:[a-z0-9-]{0,61}[a-z0-9])?$/u;

export function normalizeProvisionedHostname(input) {
  const hostname = String(input ?? '').trim().toLowerCase().replace(/\.$/u, '');
  if (!HOSTNAME_PATTERN.test(hostname)) {
    throw new Error(`Unsafe custom hostname: ${hostname || '(empty)'}`);
  }
  return hostname;
}

export function deriveProvisionerToken(appEncryptionKey) {
  const key = String(appEncryptionKey ?? '').trim();
  if (!key) throw new Error('APP_ENCRYPTION_KEY is required.');
  return createHmac('sha256', key)
    .update('minewiki:server-wiki-domain-provisioner:v1')
    .digest('base64url');
}

export function certificateNeedsRenewal(pem, now = Date.now(), renewalWindowMs = 30 * 24 * 60 * 60 * 1_000) {
  try {
    const certificate = new X509Certificate(pem);
    return Date.parse(certificate.validTo) - now <= renewalWindowMs;
  } catch {
    return true;
  }
}

export function renderNginxConfiguration({ domains, webroot, webUpstream, apiUpstream }) {
  const normalized = [...new Set(domains.map((domain) => normalizeProvisionedHostname(domain.hostname)))]
    .sort((left, right) => left.localeCompare(right));
  const ready = new Set(
    domains.filter((domain) => domain.tlsReady).map((domain) => normalizeProvisionedHostname(domain.hostname)),
  );
  const blocks = [
    '# Generated by scripts/server-wiki-domain-provisioner.mjs. Do not edit.',
    '',
  ];
  for (const hostname of normalized) {
    blocks.push(renderHttpBlock(hostname, webroot, ready.has(hostname)));
    if (ready.has(hostname)) blocks.push(renderHttpsBlock(hostname, webUpstream, apiUpstream));
  }
  return `${blocks.join('\n')}\n`;
}

function renderHttpBlock(hostname, webroot, redirectToHttps) {
  return `server {
  listen 80;
  listen [::]:80;
  server_name ${hostname};

  location ^~ /.well-known/acme-challenge/ {
    root ${webroot};
    default_type text/plain;
    try_files $uri =404;
  }

  location / {
    ${redirectToHttps ? 'return 308 https://$host$request_uri;' : 'return 404;'}
  }
}
`;
}

function renderHttpsBlock(hostname, webUpstream, apiUpstream) {
  return `server {
  listen 443 ssl;
  listen [::]:443 ssl;
  http2 on;
  server_name ${hostname};

  ssl_certificate /etc/letsencrypt/live/${hostname}/fullchain.pem;
  ssl_certificate_key /etc/letsencrypt/live/${hostname}/privkey.pem;
  include /etc/letsencrypt/options-ssl-nginx.conf;
  ssl_dhparam /etc/letsencrypt/ssl-dhparams.pem;

  client_max_body_size 8m;
  add_header X-Content-Type-Options "nosniff" always;
  add_header Referrer-Policy "strict-origin-when-cross-origin" always;
  add_header X-Frame-Options "SAMEORIGIN" always;

  location /uploads/ {
    rewrite ^/uploads/([^/]+)$ /v1/files/public/$1/raw break;
    proxy_pass ${apiUpstream};
    proxy_set_header Host $host;
    proxy_set_header X-Real-IP $remote_addr;
    proxy_set_header X-Forwarded-For $remote_addr;
    proxy_set_header X-Forwarded-Proto https;
    proxy_set_header Cookie "";
    proxy_set_header Authorization "";
    proxy_hide_header Set-Cookie;
  }

  location / {
    proxy_pass ${webUpstream};
    proxy_set_header Host $host;
    proxy_set_header X-Real-IP $remote_addr;
    proxy_set_header X-Forwarded-For $remote_addr;
    proxy_set_header X-Forwarded-Proto https;
    proxy_set_header Cookie "";
    proxy_set_header Authorization "";
    proxy_set_header X-CSRF-Token "";
    proxy_set_header X-MineWiki-Custom-Host "";
    proxy_set_header X-MineWiki-Site-Slug "";
    proxy_hide_header Set-Cookie;
  }
}
`;
}
